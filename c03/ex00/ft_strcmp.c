int		ft_strcmp(char *s1, char *s2)
{
	int a;
	int n;

	a = 0;
	while (s1[a] || s2[a])
	{
		n = s1[a] - s2[a];
		if (n != 0)
			return (n);
		a++;
	}
	return (0);
}
